package exam03;

public abstract class Duck {
    public void quack() {
        System.out.println("오리가 꽥꽥!");
    }

    public void swim() {
        System.out.println("헤엄친다.");
    }

    public void layEggs() {
        System.out.println("알을 낳는다.");
    }

    public void fly() {
        System.out.println("오리 날아간다.!");
    }
}
